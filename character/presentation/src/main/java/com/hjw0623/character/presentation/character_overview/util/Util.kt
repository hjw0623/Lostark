            }
        }
    }
    if (tier == 4 && grade == "유물") {
        for ((effectGroup, shortName) in tierFourRelicEffectWithShortName) {
            if (effectGroup.any { normalizeText(it) == normalizedSpecialEffect }) {
                return shortName
            }
        }
    }
    if (tier == 3 && grade == "고대") {
        for ((effectGroup, shortName) in tierThreeAncientEffectWithShortName) {
            if (effectGroup.any { normalizeText(it) == normalizedSpecialEffect }) {
                return shortName
            }
        }
    }
    if (tier == 3 && grade == "유물") {
        for ((effectGroup, shortName) in tierThreeRelicEffectWithShortName) {
            if (effectGroup.any { normalizeText(it) == normalizedSpecialEffect }) {
                return shortName
            }
        }
    }
    return specialEffect
}


