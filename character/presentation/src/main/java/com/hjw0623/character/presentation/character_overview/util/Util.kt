    val normalizedSpecialEffect = normalizeText(specialEffect)

    if (tier == 4 && grade == "고대") {
        for ((effectGroup, shortName) in tierFourAncientEffectWithShortName) {
            if (effectGroup.any { normalizeText(it) == normalizedSpecialEffect }) {
